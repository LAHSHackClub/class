<h3>App Data Usage</h3>
<p>This website features a "save" feature that allows you to save classes to "lists" for future reference. In order for this feature to work, we use your web browser's built-in storage functionality. We do not upload or have access to any of the data you enter into this app. Therefore, if you clear cache or storage in your browser, your data on this site will be permanently deleted. Likewise, it is currently not possible to synchronize actions made with another device.</p>
<p>Other basic information may be logged to monitor web traffic and maintain app health for all users. No personally identifiable information is collected.</p>
<h3>MVLA Data Compliance</h3>
<p>Because we do not collect any personally identifiable information, we comply with the MVLA district policy on data management.</p>

