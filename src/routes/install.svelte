
<h1>Install LAHS Classes as an App</h1>
<p>Did you know you can install LAHS Classes as an app on your computer or phone? With the app, you can take your lists on the go when you meet with your counselor, teachers, and other advisors about your classes.</p>
<h3>iPhone or iPad</h3>
<p>Open classes.lahs.plus in Safari on your Apple device. Other browsers will not work due to Apple's restrictions. Then, tap the share button, and tap "Add to Home Screen" - this will add an app icon that launches LAHS Classes.</p>
<h3>Android</h3>
<p>After navigating to classes.lahs.plus, your device may prompt you to add this page to your home screen. If so, tap the banner and continue. If not, tap your browser's page options, and find "Add to Home" and tap it.</p>
<h3>Windows, macOS, and Linux</h3>
<p>Only certain browsers on these operating systems support adding websites as apps, such as Chrome and Edge. Safari and Firefox do not support this feature. When navigating to this site, you will see an option in the top right to add this site as an app, or you may have to navigate to your browser's context menu to do so.</p>
